<p style="margin-left:0px">KEBUTUHAN tenaga medis terhadap alat pelindung diri (APD) dalam penanganan pandemi covid-19 semakin tinggi. Melihat itu, PT. Modena Indonesia ikut membantu tenaga medis dengan mendonasikan APD yang diserahkan kepada Persatuan Perawat Nasional Indonesia (PPNI)</p>

<p style="margin-left:0px">Direktur Modena Indonesia Bagus Prastowo mengatakan, donasi APD itu aekaligus bagian dari apresiasi Modena terhadap upaya keras tenaga medis Indonesia merawat pasien covid-19 di Indonesia yang terus bertambah. Bagus berharap, bantuan APD itu bisa ikut memenuhi kebutuhan APD yang tinggi.</p>

<p style="margin-left:0px">"Mendesaknya kebutuhan APD bagi petugas medis menjadi pertimbangan Modena dalam memutuskan bentuk bantuan. Sebagai garda terdepan dalam memberikan perawatan kepada pasien covid-19, APD sangat penting bagi tenaga medis untuk mengurangi risiko tertular virus korona sekaligus menjaga keselamatan dalam menjalankan tugas. Ini juga sebagai salah satu langkah untuk menahan laju penyebaran covid-19 di lingkungan tempat pelayanan medis." ujar Bagus dalam keterangan tertulisnya.</p>
<p style="margin-left:0px">Bagus menambahkan, selain ambil bagian dalam penanggulangan covid-19 lewat donasi, Modena juga menerapkan langkah-langkah untuk menekan laju penyebaran virus korona di lingkungan usaha, tanpa mengurangi kualitas pelayanan kepada  konsumen. </p>
<p style="margin-left:0px">Diantaranya, menerapkan Work From Home bagi para karyawan , penyemprotan disinfektan, serta pelaksanaan rapat berbasis internet.</p>
<p style="margin-left:0px">"Adalah tanggung jawab kita bersama dalam melawan covid-19. Modena berharap dukungan kepada para tenaga medis ini dapat memberikan manfaat, sekaligus sebagai ajakan kepada berbagai pihak untuk turut serta melindungi mereka yang berada di garis depan dalam memerangi covid-19. Menerapkan gaya hidup bersih dan sehat serta melaksanakan berbagai anjuran pemerintah, adalah langkah yang dapat kita lakukan untuk ambil bagian memutus rantai penyebaran virus korona." pungkasnya. (RO/OL-7)</p>
