<p style="margin-left:0px">Ambil bagian dalam upaya penanggulangan penyebaran virus Corona (COVID-19) yang makin mengkhawatirkan, PT. MODENA Indonesia sambangi kantor Sekretariat Persatuan Perawat Nasional Indonesia (PPNI) di  Jakarta, dengan membawa paket Alat Pelindung Diri (APD) untuk didistribusikan kepada para tenaga medis yang bertugas merawat pasien COVID-19.</p>

<p style="margin-left:0px">Dalam kunjungannya, Bagus Prastowo menyatakan bahwa MODENA sangat mengapresiasi kerja keras  para tenaga medis dalam bertugas di tengah situasi sulit akibat COVID-19. Bahkan seringkali mereka harus berjibaku merawat pasien COVID-19 dengan memakai alat perlindungan seadaanya akibat kelangkaan APD.</p>

<p style="margin-left:0px">"Mendesaknya kebutuhan APD bagi petugas medis menjadi pertimbangan MODENA dalam memutuskan bentuk bantuan. Sebagai garda terdepan dalam memberikan perawatan kepada pasien COVID-19, APD sangat penting bagi tenaga medis untuk mengurangi resiko tertular virus Corona sekaligus menjaga keselamatan dalam menjalankan tugas. Ini juga sebagai salah satu langkah untuk menahan laju penyebaran COVID-19 di lingkungan tempat pelayanan medis." ujar Bagus Prastowo, Director PT.MODENA Indonesia. </p>
<p style="margin-left:0px">Lebih lanjut Bagus Prastowo menjelaskan, selain ambil bagian dalam penanggulangan COVID-19 lewat donasinya, MODENA juga menerapkan langkah-langkah untuk menekan laju penyebaran virus Corona di lingkungan MODENA, tanpa mengurangi kualitas pelayanan kepada  konsumen. Antara lain, menerapkan <em>Work From Home</em> bagi para karyawan , penyemprotan <em>disinfectant</em>, serta pelaksanaan <em>meeting</em> berbasis internet. </p>
<p style="margin-left:0px">"Adalah tanggung jawab kita bersama dalam melawan COVID-19. MODENA berharap dukungan kepada para tenaga medis ini dapat memberikan manfaat, sekaligus sebagai ajakan kepada berbagai pihak untuk turut serta melindungi mereka yang berada di garis depan dalam memerangi COVID-19. Menerapkan gaya hidup bersih dan sehat serta melaksanakan berbagai anjuran pemerintah , adalah langkah yang dapat kita lakukan untuk ambil bagian memutus rantai penyebaran virus Corona." tutup Bagus Prastowo.</p>
