<div style="margin-left:60px">As a part of MODENA 30 years anniversary on 2011, MODENA Indonesia cooperate with Habitat For Humanity Indonesia (Habitat Indonesia) performed Corporate Social Responsibility (CSR) on May, 28 2011. MODENA and Habitat Indonesia built and renovated 33 buildings on suburb Sentul, Bogor.</div>	
</div>
</p>
<p style="margin-left:0px">These buildings comprise of houses and public facilities (such as toilets) wide spread on Village Karang Tengah, Kecamatan Babanan Madang, Kabupaten Bogor. At that moment, all employees stationed on MODENA Indonesia head office has been actively participated during the building development, especially in finishing process.</p>
<p style="margin-left:0px">Antusiasm not only showed by the employees, but also by local residents during their involvement in various programs on this event. This program has many games and also counseling intended for women and children. The games are coloring contest, cooking demonstration, quiz dan healthy living counceling for children.</p>
<p style="margin-left:0px">Aftermath, MODENA Indonesia gave donation valued IDR 150 million to Habitat Indonesia, which later given to the chief of Village Karang Tengah as representative of residents. This donation would be used for funding the whole development and renovation of 33 buildings. Happiness and good impressions along the residents giving unforgetable memories for MODENA Indonesia. </p>
