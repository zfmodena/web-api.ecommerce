<p style="margin-left:0px">Jakarta, 4 Desember 2019.  Pada pagi ini, sebuah acara unik jelang penutup tahun 2019 dipersembahkan Oleh Yayasan Kanker Anak Indonesia (YKAI) bekerjasama dengan MODENA.  Acara ini merupakan ajang penggalangan dana yang dikemas dengan konsep <em>“Cooking Class”</em> dan <em>fundraiser charity event</em> dengan menggandeng MODENA Chef yang terkenal yaitu, <b>Chef Norman Ismail.</b></p>
<p style="margin-left:0px">Acara yang terbuka untuk umum ini, dimulai sejak pagi ini ditutup dengan makan siang bersama.  Mengajak para donatur, pemerhati serta hadirin dan undangan yang peduli pada anak-anak pejuang kanker untuk memasak bersama dengan Chef dari MODENA, Chef Norman Ismail.  Chef Norman kali ini mendemonstrasikan masakan dengan menu yang telah dirancang membangkitkan selera dan tentunya sehat.  Resep masakan kali ini dipersembahkan khusus oleh Chef Norman sendiri untuk acara Charity Cooking Class bersama YKAI dan MODENA.</p>

<p style="margin-left:0px">Resep masakan yang dipraktekkan bersama dengan hadirin undangan serta media adalah <em><b>Roasted Chicken with Sauteed Mix Mushrooms</b></em> sebagai menu makanan utama dan <em><b>Strawberry Cheesecake</b></em> sebagai menu makanan penutup atau <em>dessert.</em> </p>

<p style="margin-left:0px"><b>Tujuan Charity Cooking Class bersama MODENA</b> </p>
<p style="margin-left:0px">Diselenggarakannya acara ini adalah merupakan kegiatan CSR (Corporate Social Responsibility) atau sebagai Tanggung Jawab Sosial Perusahaan dari MODENA untuk Yayasan Kanker Anak Indonesia (YKAI,) dengan memberikan secara 100% nilai nominal dari paket Kelas Memasak yang tertera pada undangan per orangnya, dan ini sebagai donasi yang diberikan sepenuhnya kepada YKAI.  Dengan demikian MODENA turut mengkampanyekan ‘Kembalikan Senyum Sehat Anak Indonesia’ yang digagas oleh YKAI sebagai kegiatan dan niat tulus untuk membantu membahagiakan kembali anak-anak penjuang kanker dari keluarga prasejahtera, agar sehat dan dapat bersekolah kembali untuk meraih mimpi dan cita-cita mereka</p>
<p style="margin-left:0px">“Terima kasih kepada MODENA yang telah memberikan kesempatan dan ruang ‘kitchen’ cantiknya untuk melaksanakan kegiatan Charity Cooking Class ini.  Setiap anak dengan kanker berhak memperoleh perawatan dan pengobatan yang layak.  Kepedulian dan bantuan yang ibu, bapak, sahabat, para donatur berikan, tentunya turut membantu memberikan harapan bagi anak-anak para pejuang kanker dari keluarga prasejahtera ini”, ujar Sallyana Sorongan, Pendiri dan Ketua Yayasan Kanker Anak Indonesia (YKAI).</p>

<p style="margin-left:0px"><b>Yayasan Kanker Anak Indonesia (YKAI)</b></p>
<p style="margin-left:0px">Yayasan Kanker Anak Indonesia (YKAI) adalah sebuah organisasi non-profit independen yang fokus membantu anak dengan kanker dari keluarga pra- sejahtera di Indonesia. Didirikan oleh Sallyana Sorongan, seorang Ibu rumah tangga yang tergerak hatinya untuk membantu anak-anak dengan kanker. Sejak 2004 membantu siapa saja yang membutuhkan dan berkembang menjadi Yayasan Sentuhan Kasih Anak Indonesia (YSKAI) pada 2010. Pada 2018, nama dan logo YSKAI akan berubah menjadi YKAI namun tetap mengusung visi dan misi yang sama. </p>
<p style="margin-left:0px">Program Senyum Anak Sehat Indonesia adalah gagasan utama yang diwujudkan melalui program Pengobatan, Transportasi dan dukungan untuk prang tua dari anak – anak dengan kanker. YKAI percaya bahwa setiap anak dengan kanker berhak mendapatkan perawatan dan pengobatan yang layak. Diharapkan dengan Program Senyum Anak Sehat Indonesia, anak-anak dapat kembali tersenyum dan sehat</p>


