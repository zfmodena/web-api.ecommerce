<p style="margin-left:0px">Kegiatan ini merupakan bagian dari program CSR MODENA yang dilaksanakan secara berkala. Perusahaan dengan aktif melakukan kegiatan sosial sebagai wujud kepedulian sosial kepada masyarakat, khususnya warga yang kurang beruntung. </p>
<p style="margin-left:0px">Beberapa kegiatan sejenis yang telah dilakukan oleh MODENA Indonesia pada tahun-tahun sebelumnya meliputi penyaluran bantuan berupa berbagai perangkat rumah tangga, seperti peralatan mesin cuci, penanak nasi, blender, alat setrika dan lain sebagainya. Bantuan tersebut telah diserahkan kepada beberapa panti asuhan dan panti jompo di wilayah Jakarta dan sekitarnya, diantaranya Panti Asuhan Abhimata dan Tri Asih.</p>
