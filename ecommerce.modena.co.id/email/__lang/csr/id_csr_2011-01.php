<div style="margin-left:60px">Sebagai bagian dari program 30 Tahun MODENA yang jatuh tepat di tahun 2011 ini MODENA Indonesia kembali melaksanakan program Corporate Social Responsibilty (CSR) yang kali ini bekerjasama dengan Yayasan Habitat For Humanity Indonesia (Habitat Indonesia).</div>	
</div>
</p>
<p style="margin-left:0px">Dilaksanakan pada tanggal 28 Mei 2011 lalu, MODENA bersama Habitat Indonesia melakukan pembangunan dan renovasi 33 bangunan di wilayah pinggiran Sentul Bogor. Bangunan yang digarap meliputi rumah tinggal dan fasilitas umum (kamar mandi dan toilet) yang tersebar di Desa Karang Tengah Kecamatan Babanan Madang, Kabupaten Bogor. Pada kesempatan ini, seluruh karyawan kantor pusat MODENA Indonesia turut berperan aktif, khususnya dalam melakukan proses finishing  bangunan.</p>
<p style="margin-left:0px">Antusiasme tidak hanya diperlihatkan oleh para karyawan namun juga oleh masyarakat setempat saat mereka berkesempatan mengikuti berbagai program yang digelar pada hari yang sama. Kegiatan, perlombaan dan permainan menarik juga sarat informasi ditujukan bagi para ibu dan anak warga Desa Karang Tengah, termasuk di dalamnya lomba mewarnai anak, demo dan lomba memasak, lomba meniup balon bagi para ibu, kuis berhadiah serta penyuluhan tentang kebersihan kuku untuk anak.</p>
<p style="margin-left:0px">Sebagai puncak acara, MODENA Indonesia menyerahkan donasi sebesar Rp.150.000.000,- kepada Habitat Indonesia untuk kemudian diteruskan kepada kepala Desa Karang Tengah sebagai perwakilan dari warga untuk biaya pembangunan dan renovasi ke-33 bangunan yang telah dilakukan. Ekspresi kebahagiaan masyarakat sepanjang acara memberikan kesan tersendiri yang tak terlupakan bagi MODENA Indonesia beserta karyawan. </p>
