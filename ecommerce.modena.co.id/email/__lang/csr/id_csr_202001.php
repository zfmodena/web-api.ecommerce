<p style="margin-left:0px">Begerak cepat membantu korban banjir yang terjadi di Jakarta dan sekitarnya, PT. MODENA Indonesia menyambangi beberapa titik lokasi pengungsian, dengan memanfaatkan armada MODENA  Mobile Clinic Solution. </p>

<p style="margin-left:0px">Membawa berbagai kebutuhan bagi masyarakat yang terkena dampak banjir, MODENA memfokuskan bantuan bagi para balita dan anak – anak yang terkonsentrasi pada titik – titik pengungsian di Cililitan, Rawajati, Pancoran, Tebet, Cipadu, serta Cilandak.</p>

<p style="margin-left:0px">“Balita dan anak-anak yang terkena dampak banjir sangat rentan terganggu kesehatannya. Penting bagi mereka untuk tetap mendapatkan asupan nutrisi yang baik. Namun dalam kondisi darurat, hal ini sering tidak memungkinkan. Inilah yang menjadi pertimbangan MODENA untuk ikut ambil bagian memastikan terpenuhinya kebutuhan nutrisi bagi para balita dan anak-anak, dengan menyediakan makanan berstandar UNICEF ”  ujar Bagus Prastowo, Senior Vice President Customer Management PT.MODENA Indonesia.</p>

<p style="margin-left:0px">Untuk menyediakan makanan yang memiliki kualitas nutrisi yang baik, MODENA menggandeng  Asosiasi Ibu Menyusui Indonesia (AIMI). Ini tak lepas dari komitmen AIMI selalu menggunakan standar WHO dan UNICEF, serta pemenuhan kriteria Piramida Gizi Seimbang sesuai dengan ketentuan Kementrian Kesehatan sebagai panduan dalam memberikan asupan nutrisi bagi bayi dan anak-anak.</p>
<p style="margin-left:0px">“Pengalaman AIMI dalam hal pemberian jenis makanan yang tepat bagi balita dan anak-anak untuk menghadapi berbagai kondisi, serta semangat <em>Live Healthy Be Happy</em> yang diusung MODENA, menjadi sebuah keselarasan dalam upaya mendukung terpenuhinya nutrisi yang baik dan layak bagi balita dan anak-anak korban banjir” lanjut Bagus. </p>
<p style="margin-left:0px">Nia Oemar selaku Ketua Umum AIMI menyatakan, langkah MODENA dalam menunjukan kepedulian bagi balita dan anak-anak korban banjir patut diapresiasi, karena dalam kondisi sulit, kebutuhan nutrisi yang baik bagi anak sering kali luput dari perhatian.</p>
<p style="margin-left:0px">“AIMI berharap kontribusi MODENA dapat memberikan inspirasi banyak pihak untuk turut serta memberikan perhatian lebih bagi anak-anak kita terutama dalam kondisi yang sulit seperti sekarang.” ujar Nia.</p>
<p style="margin-left:0px">Lebih jauh mengenai dampak banjir yang terjadi tahun ini, MODENA tak lupa menaruh perhatian bagi para konsumennya. Banjir tinggi yang terjadi mengakibatkan banyak unit MODENA milik konsumen memerlukan perbaikan.</p>
<p style="margin-left:0px">Oleh karena itu,berbagai program akan disiapkan MODENA sebagai salah satu bentuk upaya MODENA untuk menjaga kepuasan dan loyalitas konsumen.</p>
<p style="margin-left:0px">Bagus mengungkapkan bagi konsumen yang memerlukan perbaikan unit MODENA akibat dampak banjir untuk tidak ragu menghubungi layanan <em>customer service</em> MODENA di 1500715.</p>
<p style="margin-left:0px">“Sebagai bagian dari masyarakat, MODENA merasa perlu untuk ikut ambil bagian  bersama-sama menanggulangi dampak bencana. Kami berharap, kontribusi yang kami berikan dapat memberikan manfaat bagi masyarakat, terutama dalam upaya untuk tetap menjaga hidup sehat sesuai dengan slogan <em>Smart Living</em> yang selalu tersemat pada MODENA.” pungkasnya.</p>
