<p style="margin-left:0px">Salah satu bentuk kerjasama yang disepakati adalah pengadaan Sarana dan fasilitas pembelajaran berupa TRISAKTI-MODENA Kitchen Studio di STP Trisakti Bintaro. Dan pada hari ini Selasa 6 Maret 2018 dapat diwujudkan dengan Pelaksanaan Acara Peresmian Trisakti – Modena Kitchen Studio.</p>

<p style="margin-left:0px">Kitchen Studio ini bertujuan untuk mengakomodir proses belajar mengajar di STP Trisakti dalam mata kuliah “Pengolahan Makanan”. Dengan sarana dan perangkat masak yang modern dalam Kitchen Studio ini diharapkan pengetahuan mahasiswa tentang perangkat masak akan menjadi lebih baik untuk kedepannya seiring dengan kemajuan teknologi pengolahan makanan yang semakin maju hari demi hari.</p>

<p style="margin-left:0px">Acara Peresmian diawali dengan sambutan dari Ibu Fetty Asmaniati, SE.MM selaku Ketua STP Trisakti, dilanjutkan sambutan oleh Bapak Robert Widjaja Direktur MODENA Indonesia, dilanjutkan dengan ,seremoni peresmian dan diakhiri dengan Healthy Cooking Demo oleh MODENA Chef Steby Rafael. Para undangan yang hadir dapat mencicipi masakan yang sehat dan bercita rasa.</p>

<p style="margin-left:0px">Semoga kerjasama yang telah terjalin dengan baik ini dapat terus dipertahankan dan menjadi lebih baik lagi dimasa yang akan datang.</p>
