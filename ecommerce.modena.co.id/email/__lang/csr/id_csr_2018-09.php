<p style="margin-left:0px">Jumat 28 September 2018, gempa bumi 7,4 Scala Richter melanda Palu-Donggala Sulawesi Tengah disertai tsunami dan tanah amblas (likuifaksi). Diperkirakan 2,256 korban jiwa meninggal dunia, 223,751 orang terlantar, dan 68,451 rumah rusak, menurut data terkini <em>Rescuers Search for Survivor in Palu.</em></p>
<p style="margin-left:0px">Demi membangun kembali kehidupan dan harapan kepada saudara-saudara kita di Palu, MODENA memberikan dana bantuan kemanusia melalui <em>Habitat for Humanity</em> Indonesia. Pemberian bantuan dilaksanakan pada tanggal 30 November 2018, diserahkan secara langsung oleh Direktur MODENA Indonesia Robert Widjaja kepada Susanto Samsudin selaku <em>National Director Habitat for Humanity</em> Indonesia.</p>

<p style="margin-left:0px">“Sebelumnya kami telah mengirimkan bantuan <em>MODENA Care For Palu & Donggala</em> melalui yayasan dan rekanan MODENA. Mengingat <em>impact</em> bencana cukup panjang dan membutuhkan waktu lama untuk pemulihan, kami memikirkan apa lagi yang bisa kami berikan untuk Palu, maka <em>Habitat for Humanity</em> kami percayakan untuk mengelola bantuan jangka panjang.” jelas Novi Hariyanti, <em>Marketing Group Head</em> MODENA Indonesia.</p>
<br /><p style="margin-left:0px">Rencananya bantuan jangka panjang akan dimanfaatkan untuk membangun rumah-rumah transisi di Desa Dolo Selatan, Distrik Sigi supaya keluarga yang terkena dampak bencana bisa segera menjalankan fase pemulihan. Rumah transisi yang juga dapat dibangun secara permanen ini merupakan solusi jangka panjang terhadap kebutuhan hunian keluarga demi terbentuknya komunitas yang kuat di kemudian hari./p>
