<p style="margin-left:0px">MODENA, yang juga memiliki kantor cabang di Kota Gudeg juga di Kota Pahlawan, menyerahkan donasi rumah secara simbolis kepada Habitat for Humanity Indonesia (Yogyakarta) yang kemudian dilanjutkan kepada enam warga penerima manfaat. </p>
<p style="margin-left:0px">Sebagai wilayah yang berlokasi pinggiran kali Code, warga Jogoyudan telah menjadi korban imbas banjir lahar dingin Merapi. Terdapat banyak rumah yang tertimbun lahar dingin dengan kondisi yang memprihatikan. Sedang Dukuh Kupang Utara memiliki warga dengan latar belakang ekonomi yang beragam. Meski penghasilan mereka didominasi dari sektor informal, di wilayah ini masih terdapat kebutuhan hunian sederhana dan sehat yang belum terpenuhi, khususnya bagi penduduk yang berpenghasilan rendah.</p>
<p style="margin-left:0px">Prakarsa ini merupakan bagian dari program Corporate Social Responsibility (CSR) berkelanjutan dengan tajuk Our Care for Society yang dicanangkan MODENA sejak tahun lalu. Sebelumnya di awal 2011, MODENA Indonesia telah menyelenggarakan pembangunan serupa di wilayah pinggiran Sentul, Bogor. “Ini merupakan komitmen kami untuk memberikan kontribusi balik yang dapat membawa manfaat bagi masyarakat khususnya kalangan yang membutuhkan,” tegas Lanny Kurniawan, Customer Management Division Head MODENA Indonesia. </p>
<p style="margin-left:0px">MODENA sebagai perusahaan penyedia home appliances yang hadir di Indonesia lebih dari 30 tahun, sangat memahami bahwa sebuah hunian layak dan nyaman mampu membawa pengaruh positif bagi penghuninya. Terpenuhinya kebutuhan dasar mendorong setiap orang meraih kebutuhan-kebutuhan lanjutan guna menunjang kualitas hidup yang lebih baik.</p>
<p style="margin-left:0px">Lebih dari 50 karyawan MODENA di Yogyakarta dan Surabaya terlibat dalam kegiatan sehari sebagai relawan untuk melakukan fase akhir pembangunan. “Karyawan sangat antusias terlibat dalam proses finishing rumah warga. Ini menjadi bentuk andil lain untuk membantu kalangan yang membutuhkan di sekitar kami,” lanjut Lanny. </p>
<p style="margin-left:0px">Tidak hanya pemberian donasi dan kegiatan volunteering, pada kesempatan yang sama MODENA juga menyampaikan penyuluhan kesehatan kulit, tangan, kaki, gigi dan rambut kepada ibu-ibu warga setempat dengan melibatkan ibu PKK wilayah tersebut, juga kegiatan demo masak, kompetisi masak sampai dengan perlombaan bagi anak-anak.</p>
<p style="margin-left:0px">Lanny menuturkan di sela-sela kegiatan CSR, “Kesamaan visi untuk mewujudkan hunian layak, mendorong MODENA bekerja sama dengan Habitat for Humanity Indonesia yang telah berpengalaman membangun puluhan ribu hunian di Indonesia. Program pengadaan rumah tinggal ini diharapkan memberikan andil dalam penyediaan solusi akan hunian yang memadai, khususnya bagi para keluarga penerima manfaat sehingga taraf dah kualitas hidup mereka lebih meningkat.”</p>				</td>
