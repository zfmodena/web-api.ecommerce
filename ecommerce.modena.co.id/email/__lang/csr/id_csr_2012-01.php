<div style="margin-left:60px">MODENA Indonesia menyampaikan bantuan donasi untuk pembangunan rumah layak tinggal bagi warga di Jogoyudan, Desa Gowongan, Kecamatan Jetis (Yogyakarta) dan warga di Dukuh Kupang Utara, Kecamatan Sawahan (Surabaya). Pembangunan rumah tinggal inisiatif MODENA ini telah dimulai sejak akhir Januari lalu dan merupakan hasil kerja sama dengan Habitat for Humanity (HFH)  Indonesia, sebuah lembaga non-profit yang berfokus pada penyediaan hunian layak bagi kalangan tidak mampu atau yang tertimpa bencana. Melalui rangkaian acara CSR yang melibatkan partisipasi seluruh karyawan MODENA cabang Yogyakarta dan Surabaya, telah dirampungkan total 6 buah rumah baru siap huni bagi warga di kedua wilayah tersebut.</div>	
