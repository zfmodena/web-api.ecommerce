<div style="margin-left:60px">Rumah menjadi tempat bernaung bagi para anggota penghuninya. Di dalam rumah pula interaksi antar penghuni terbentuk, baik untuk menambah kehangatan pun pembelajaran. Namun jika kondisi rumah tidak layak tinggal, maka ketidaknyamanan yang ditimbulkan dapat mempengaruhi kualitas hidup berikut interaksi penghuninya. Didasari oleh kesadaran penuh atas pentingnya sebuah hunian yang layak guna menunjang terciptanya keluarga berkualitas, MODENA sebagai perusahaan penyedia peralatan rumah tangga terpanggil untuk mengambil langkah nyata mewujudkan kebutuhan papan yang layak bagi keluarga yang saat ini masih tinggal di rumah yang tidak layak huni, Prakarsa ini merupakan program CSR "Our Care for Society" yang dilakukan setiap tahunnya.</div>	
</div>
</p>
<p style="margin-left:0px">Tahun 2013 ini pembangunan dan renovasi rumah layak huni diperuntukkan bagi warga Kelurahan Belawan Sicanang, Medan (tiga rumah) dan Kampung Babakan Cinta, Pasirhalang, Bandung (tiga rumah). Pembangunan proyek rumah ini telah berlangsung sejak bulan Januari lalu. Uniknya, karyawan MODENA khususnya di Kantor Cabang Medan dan Bandung turut berpartisipasi aktif di fase akhir pembangunan (<em>finishing</em>) dengan melakukan proses pengecatan rumah. Kegiatan <em>volunteering</em> ini dilakukan di Medan dan Bandung masing-masing tanggal 16 Februari lalu dan 23 Februari 2013, yang kemudian dilanjutkan dengan penyerahan rumah jadi dari MODENA kepada para penerima manfaat.</p>
<p style="margin-left:0px">&quot;Sebagai tempat dimulainya hampir segala bentuk aktivitas, sebuah rumah yang layak secara kondisi fisik maupun situasi di dalamnya menjadi salah satu elemen penting dalam pembangunan pribadi penghuninya. Program CSR ini menjadi kendaraan bagi kami untuk turut mendukung terciptanya keluarga berkualitas melalui penyediaan hunian layak tinggal dengan memperhatikan kaedah kelayakan serta kesehatan, Keterlibatan karyawan MODENA sebagai relawan dalam proses <em>finishing</em> pun tidak luput dari memberikan manfaat, yaitu guna menyentuh nurani karyawan dan mengasah rasa empati serta kepedulian terhadap sesama khususnya terhadap mereka yang kurang beruntung,&quot; papar Lanny Kurniawan, Head of Customer Management Division, MODENA Indonesia.</p>
<p style="margin-left:0px">Penerima manfaat program CSR MODENA 2013 berupa rumah layak huni di Kampung Babakan Cinta, Desa Pasirhalang, Bandung terdiri dari 3 keluarga. Pertama adalah Ibu Anah (70), seorang buruh yang tidak memiliki pekerjaan tidak tetap. Penerima bantuan lain adalah Ibu Aneng (32) yang tinggal bersama empat orang anaknya serta memiliki penghasilan dari hasil memasang payet pada pakaian. Selanjutnya pasutri Bapak Awat (45) dan Ibu Adang, yang juga berprofesi sebagai buruh dengan penghasilan rata-rata Rp 10.000 per hari pun turut mendapatkan bantuan pembangunan rumah tinggal.</p>
<p style="margin-left:0px">Tiga penerima bantuan di Kelurahan Belawan Sicanang, Medan adalah pasutri Bapak Parlindungan Napitupulu (40) dan Ibu Nerlin br Purba (35) dengan empat anak dan berprofesi sebagai buruh tukang dan pencari cacing, Berikutnya Bapak Sudarsono seorang nelayan dengan 6 orang anak, serta pasutri Bapak Mujiran (70) dan Mujinem (58) yang keduanya tidak memiliki penghasilan tetap. Dengan pemasukan rata-rata Rp 20.000 per hari tentunya, sangat sulit bagi mereka memenuhi kebutuhan papan yang layak. </p>
<p style="margin-left:0px">Seperti pada dua tahun sebelumnya, kegiatan CSR "Our Care for Society" ini kembali bekerjasama dengan Habitat for Humanity, sebuah lembaga nirlaba yang berfokus pada penyediaan hunian layak bagi masyarakat kurang beruntung ataupun korban bencana.</p>
<p style="margin-left:0px">Lanny Kurniawan lalu menuturkan, "Kami menggandeng Habitat for Humanity dalam menyalurkan bantuan renovasi dan pembangunan rumah layak huni karena kesamaan visi terkait penyediaan hunian layak bagi mereka yang berasal dari kalangan membutuhkan. Selain saluran bantuan melalui Habitat for Humanity, MODENA juga melibatkan fans di laman media sosial facebook dan twitter melalui aplikasi Our Care for Society. Fans serta <em>follower</em> cukup memilih salah satu bahan bangunan yang tersedia pada aplikasi untuk mewujudkan rumah layak huni.”</p>				
<p style="margin-left:0px">Selain kegiatan <em>volunteering </em>yang melibatkan karyawan MODENA untuk proses <em>finishing</em> rumah, juga diselenggarakan rangkaian acara edukatif berupa penyuluhan bagi ibu dan lomba mewarnai bagi anak yang merupakan warga setempat. Acara ini juga turut dimeriahkan oleh Chef Lucky Andreono (Pemenang Master Chef Indonesia Season 1) yang memberikan pembekalan berupa demo masak terkait penyajian hidangan sehat.
 </p>		
 
 <p style="margin-left:0px">Inisiatif program CSR – Our Care For Society – ini telah dimulai sejak 2011 lalu. Awalnya, MODENA mewujudkan rumah layak huni berikut fasilitas umum bagi warga pinggiran di wilayah Sentul, Bogor. Kemudian dilanjutkan di 2012 giliran untuk warga Kampung Dukung Kupang Utara, (Surabaya) serta warga Jogoyudan (Jogjakarta) menjadi penerima manfaat dalam bentuk pembangunan rumah tinggal. Dan dalam acara dengan beragam program baik aktivititas building berupa pengerjaan tahapan akhir pembangunan rumah serta non building berupa acara yang dikemas padat, menghibur dan edukatif bagi ibu dan anak, diharapkan mampu membawa semangat baru bagi warga dalam menjelang masa depan yang lebih baik.</p>
