<p align="justify">MODENA kembali mempersembahkan "Masterpiece Retrofridge" – koleksi karya seni terbatas dari lemari pendingin seri Retro yang menjadi medium ekspresi seniman Indonesia. Setelah tahun lalu menggandeng Kemal Ezedine, Radi Arwinda, dan Hendra "Hehe" Harsono, tahun ini MODENA berkolaborai dengan Darbotz, Indra Dodi, dan Naufal Abshar. Ketiga pelukis menggoreskan <em>passion</em>, imajinasi, dan pengalamannya ke tingkatan di mana proses kreativitas menyatu dalam kanvas yang sama dengan kekuatan desain.</p>
<p align="justify">“MODENA menghadirkan kembali Masterpiece Retrofridge dengan pesan bahwa karya seni tidak hanya bisa dinikmati terbatas pada kolektor, kurator, atau seniman, namun juga dapat dinikmati secara inklusif oleh masyarakat luas”, papar Robert Widjaja, Direktur MODENA Indonesia.</p>

<p align="justify">Masterpiece Retrofridge terdiri atas tiga buah mahakarya yang dihasilkan melalui tiga proses kreatif berbeda.</p>

<p align="justify">“SUBCONSCIOUS” dari Darbotz terilhami bagian dari pikiran yang tidak sepenuhnya sadar, namun mempengaruhi tindakan dan perasaan seseorang. Ketika Darbotz pergi keluar melukis, tanpa sadar ia memilih tempat, menentukan warna, dan melakukan apapun yang ia inginkan. “Perumpamaan sederhana yang sama ketika Anda lapar di malam hari dan membuka kulkas, makan apa pun yang tersisa di dalamnya,” jelasnya.</p>

<p align="justify">Indra Dodi merefleksikan pengalaman hidupnya melalui figur imajiner serta aksentuasi kata dan warna dalam “The Big Man Story”, sebuah sosok superhero yang memiliki pengaruh besar kepada orang lain. Wajah-wajah pada lukisan Indra Dodi memvisualisasikan ceritanya sendiri. Penampilan fisik adalah metafora dari suasana kejiwaan yang menjabarkan karakter spesifik dan <em>attitude</em> seseorang. “Manusia boleh tinggi atau pendek, gemuk atau kurus,” ungkap Indra Dodi, “yang membuat perbedaan adalah ekspresi.”</p>

<p align="justify">Naufal Abshar membuat ilustrasi yang diberi judul : “<em>Timeless Robot</em>”, nostalgia masa kecil tentang robot yang senantiasa tersimpan dalam memori. Karya ini menunjukkan pergeseran waktu bagaimana kemanusiaan berkembang. Kotak mainan adalah saksi kemajuan teknologi. Bukan hanya sebatas mainan, ia mewakili imajinasi, ekspresi, dan identitas. “Kami menyebutnya mainan Retro,” ujar Naufal.</p>

<p align="justify">Masyarakat dapat melihat langsung jajaran produk Masterpiece Retrofridge di gelaran Art Jakarta  tanggal 02-05 Agustus 2018 di The Ritz Calton, Jakarta Pacific Place. Pada hari terakhir tanggal 05 Agustus di <em>main stage</em> akan diadakan <em>Art Charity Auction,</em> yang hasilnya akan didonasikan kepada Yayasan Doctor Share untuk <em>Floating Hospital.</em></p>

