<p align="justify">Pria kelahiran Padang tahun 1980 ini mulai belajar melukis dari pamannya saat menginjakkan kaki di Yogjakarta tahun 2000. Lima tahun kemudian, ia menempuh pendidikan Seni Rupa di Institut Seni Indonesia Yogyakarta.</p>
<p align="justify">Indra Dodi mengembangkan bahasa visual tersendiri. Kata-kata atau huruf dalam kanvas, tidak ditujukan untuk dimengerti, lebih merupakan elemen visual. Garis mengalir seperti sungai dan huruf-huruf berwarna bersemi seolah berada di kebun bunga. Lukisan Indra Dodi pada prinsipnya adalah sebuah puisi visual. Ia menuntun kita menutup mata kembali ke alam kanak-kanak. Keceriaan dan kemauan anak-anak berpetualang dalam dunia yang belum dikenal merupakan sumber inspirasi, motivasi, sekaligus kontributor utama dari figur-figur yang muncul di lukisan.</p>
<p align="justify">Indra menyukai permainan. Ia berpendapat bahwa manusia harus menyempatkan diri bermain-main, dengan begitu manusia akan menjadi manusia seutuhnya. “Respons dari rangsangan yang menyenangkan dari bermain sama dengan mengikuti kebenaran hati nurani,” katanya, “Seni hanya meng-<em>copy</em>nya. Seni yang berangkat dari permainan akan menyebarkan kesenangan buat semua orang.”</p>
