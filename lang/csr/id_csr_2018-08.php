<p align="justify">Darbotz sangat mencintai Jakarta. Kemacetan, kesemrawutan, dan hiruk-pikuk yang dihadapi sehari-hari membangkitkan visualisasi unik dalam karyanya : cumi-cumi.</p>

<p align="justify">Jejak awal Darbotz di jalanan ibukota adalah nama geng sekolahnya era kenakalan remaja tahun 1997. Setelah lebih memahami prinsip visual secara komprehensif selama 4 tahun kuliah, Darbotz berupaya menciptakan karakteristik visual sebagai identitas khas, tanpa harus mencantumkan nama dalam lukisan, terinspirasi penampilan salah satu grup musik <em>hip-hop</em> Amerika.</p>
<p align="justify">Darbotz hanya menggunakan warna hitam - putih dengan sedikit aksentuasi. Gaya visualisasi ini adalah bagian dari strategi yang dia gunakan selama bekerja di jalanan. Penggunaan warna-warna sederhana justru menarik perhatian yang lebih intens. Beberapa perusahaan multinational tertarik berkolaborasi, di antaranya : Nike dan Google Chrome. Pola, teknik, dan <em>energy</em> yang digunakan Darbotz di jalanan tersebut dituangkannya ke atas kanvas. “Bedanya, kanvas merupakan media yang lebih kecil. Sementara di jalanan,” menurut Darbotz,”…tidak ada batas…. kita dapat mencium bau jalanan, polusi, angin, macet, dan bunyi mobil lalu-lalang.”</p>
<p align="justify">Darbotz merupakan salah satu pendiri Tembokbomber.com. salah satu komunitas graffiti terbesar Indonesia.</p>

