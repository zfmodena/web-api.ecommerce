<p align="justify">Naufal Abshar lahir di Bandung 13 Juli 1993. Mengeyam pendidikan di Lasalle College of the Arts dan Goldsmith University of London di Singapura, Naufal telah berpartisipasi di berbagai pameran di Singapura, Yogyakarta, Venesia, dan Lithuania. Dengan seri “HAHA’-nya, Naufal menjelajahi akar dan batas humor serta tawa. Pada tahun 2013, Naufal berhasil memenangi <em>live painting competition</em> Indonesia Arts Festival. </p>
<p align="justify">Sebagai seorang artis, Naufal sedikit banyak terpengaruh Henri Bergson, seniman Perancis yang melakukan riset mendalami humor dan kaitannya dengan aspek sosial.</p>
<p align="justify">“Saya mengeksplorasi konsep humor dan tawa, sesuatu yang bersifat universal. ‘HAHA’ mewakili tawa dalam keseharian kita, tertawa karena memenangi sesuatu, ingin menggoda atau menertawakan seseorang, awal kegilaan atau penawar rasa takut. Saya mencoba memasukkan unsur humor dan tawa dalam melakukan kritik sosial. Humor politik dapat ditemukan dalam sebagian besar karya saya. Penggunaan <em>satiric laughter</em> memberikan gambaran bagaimana saya menggunakan tawa sebagai media berekspresi dalam menyampaikan kritik.”</p>

