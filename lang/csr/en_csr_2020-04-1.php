<p style="margin-left:0px"><strong>Marketing</strong> - Mendesaknya kebutuhan Alat Pelindung Diri (APD) bagi petugas medis menjadi pertimbangan Modena dalam memutuskan bentuk bantuan. Terlebih, sebagai garda terdepan dalam memberikan perawatan kepada pasien Covid-19, APD sangat penting bagi tenaga medis untuk mengurangi resiko tertular virus Corona sekaligus menjaga keselamatan dalam menjalankan tugas</p>

<p style="margin-left:0px">Bagus Prastowo, Director PT. Modena Indonesia mengatakan, bahwa Modena turut ambil bagian dalam upaya penanggulangan penyebaran virus Corona (Covid-19) yang makin mengkhawatirkan. Upaya yang dilakukan Modena adalah menyambangi kantor Sekretariat Persatuan Perawat Nasional Indonesia (PPNI) di Jakarta, dengan membawa paket APD untuk didistribusikan kepada para tenaga medis yang bertugas merawat pasien Covid-19.</p>

<p style="margin-left:0px">"Modena sangat mengapresiasi kerja keras para tenaga medis dalam bertugas di tengah situasi sulit akibat Covid-19. Bahkan seringkali mereka harus berjibaku merawat pasien Covid-19 dengan memakai alat perlindungan seadaanya akibat kelangkaan APD. Ini juga sebagai salah satu langkah untuk menahan laju penyebaran Covid-19 di lingkungan tempat pelayanan medis," ujar Bagus.</p>
<p style="margin-left:0px">Selain ambil bagian dalam penanggulangan Covid-19 melalui donasi, dia menambahkan, Modena juga menerapkan langkah-langkah untuk menekan laju penyebaran virus Corona di lingkungan Modena - tanpa mengurangi kualitas pelayanan kepada konsumen. Termasuk juga menerapkan <em>work from home</em> bagi para karyawan, penyemprotan <em>disinfectant</em>, serta pelaksanaan <em>meeting</em> berbasis internet.</p>
<p style="margin-left:0px">"Ini merupakan tanggung jawab bersama dalam melawan Covid-19. Kami berharap dukungan kepada para tenaga medis ini dapat memberikan manfaat, sekaligus sebagai ajakan kepada berbagai pihak untuk turut serta melindungi mereka yang berada di garis depan dalam memerangi Covid-19," imbuh dia.</p>
