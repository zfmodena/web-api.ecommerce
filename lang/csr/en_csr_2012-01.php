<div style="margin-left:60px">MODENA Indonesia submits donation for the construction of decent houses for residents living in Jogoyudan, Gowongan Village, District Jetis (Yogyakarta) and residents in the Hamlet of North Kupang, Sawahan (Surabaya). MODENA housing development initiatives have been started since the end of January and is the result of collaboration with Habitat for Humanity (HFH) Indonesia, a non-profit organization that focuses on providing decent housing for the poor or are not affected. CSR through a series of events involving the participation of all employees MODENA Yogyakarta and Surabaya branch, has completed a total of 6 pieces ready occupied new homes for residents in both areas.</div>	
