<p style="margin-left:0px">MODENA, which also has a branch office in the City gudeg also in City of Heroes, symbolically handed over the donation to Habitat for Humanity Indonesia (Yogyakarta) and then continued to the six resident beneficiaries.</p>
<p style="margin-left:0px">As the region is located outskirts times Code, Jogoyudan citizens have been victims of Merapi cold lava-induced floods. There are many houses buried by cold lava with alarming conditions. Being Hamlet North Kupang have people with diverse economic backgrounds. Although their income from the informal sector dominated, in this region there is still a need for a simple and healthy occupancy unmet, particularly for low-income residents.</p>
<p style="margin-left:0px">This initiative is part of its Corporate Social Responsibility (CSR) with a continuous canopy Care for Our Society, announced MODENA since last year. Previously in early 2011, MODENA Indonesia has organized a similar development in suburban areas Sentul, Bogor. "This is our commitment to contribute to turning that can bring benefits to the community, especially people in need," said Lanny Kurniawan, Customer Management Division Head MODENA Indonesia.</p>
<p style="margin-left:0px">MODENA as a provider of home appliances are present in Indonesia for more than 30 years, it is understood that a decent and comfortable shelter is able to bring a positive impact to the occupants. Fulfillment of basic needs to encourage everyone grabbed needs continued to support a better quality of life.</p>
<p style="margin-left:0px">More than 50 employees MODENA in Yogyakarta and Surabaya engage in daily activities as a volunteer to do the final phase of development. "The employees are very enthusiastic about engaging in the process of finishing houses. It becomes another form of contribution to help people in need around us, "said Lanny. </p>
<p style="margin-left:0px">Not only giving donations and volunteering activities, on the same occasion MODENA also deliver skin health education, hands, feet, teeth and hair to mothers locals PKK involving the region, as well as activities cooking demonstrations, competitions cook until the race for children.</p>
