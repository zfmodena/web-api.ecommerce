<p style="margin-left:0px">Jelang medio 2016, telah dilakukan pembangunan tujuh buah rumah layak huni  serta renovasi sebuah fasilitas perpustakaan sekolah TK/SD Yayasan Pendidikan Islam Serbaguna, di wilayah Tegalsari, Surabaya, dimana proyek ini digawangi oleh HFHI. Pada kesempatan ini pula, seluruh karyawan MODENA cabang Surabaya turut berperan aktif, khususnya dalam proses akhir (<em>finishing</em>) pembangunan.</p>

<p style="margin-left:0px">Antusiasme tidak hanya ditunjukkan oleh para karyawan yang bertindak sebagai <em>volunteer</em>, namun juga dirasakan dari warga lingkungan penerima manfaat. Kegiatan lomba mewarnai bagi para siswa/siswi PAUD dan kegiatan demo memasak yang diikuti para ibu-ibu PKK setempat makin menambah keceriaan pelaksanaan kegiatan CSR MODENA.</p>

<p style="margin-left:345px">Acara CSR diakhiri dengan penyerahan simbolis pembangunan rumah layak huni dan juga renovasi sekolah kepada para penerima manfaat. Ekspresi kebahagiaan masyarakat sepanjang acara memberikan kesan tersendiri yang tak terlupakan.</p>

<p style="margin-left:345px">Komitmen MODENA untuk mewujudkan kepedulian sosial dan berbagi kepada sesama  akan terus dituangkan dalam program tahunan Corporate Social Responsibility yang berkelanjutan di waktu mendatang.</p>
