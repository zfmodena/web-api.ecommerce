<div style="margin-left:0px">Di pertengahan tahun 2016, MODENA menghadirkan seri produk terbarunya Maestro Chefs Limited Collection Gas Hob. Kompor tanam edisi terbatas ini mengusung misi kemanusiaan dan menjadi bagian dari inisiatif Corporate Social Responsibility bekerjasama dengan Habitat For Humanity Indonesia (HFHI). Hasil dari penjualan kompor tanam yang juga dibubuhi tandatangan dua Chef kenamaan Indonesia (Chef Marinka dan Chef Bara Pattiradjawane) ini didonasikan untuk pembangunan rumah layak huni bagi keluarga kurang beruntung. </div>
