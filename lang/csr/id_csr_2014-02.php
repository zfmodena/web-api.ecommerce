<div style="margin-left:0px"><b>Jakarta 7 Febuari 2014</b> – Banjir yang melanda kota Manado, Sulawesi Utara, serta banjir yang menggenangi ibukota Jakarta dan juga beberapa kota lainnya di Indonesia merupakan bencana yang amat memilukan. Kala air deras datang dengan tiba-tiba, memenuhi jalan hingga masuk ke dalam hunian, penghuni rumah tentunya akan berupaya untuk menyelamatkan diri serta anggota keluarganya tanpa memikirkan hal lain  seperti harta benda yang ada di dalam rumah yang tertinggal dalam genangan air. Setelah banjir mereda dan air perlahan surut, penghuni kembali ke rumah untuk membereskan dan membersihkan isi hunian atau bahkan memperbaiki rumah berikut beragam perabotan dan perangkat rumah tangga yang rusak diterjang banjir. Tidak terhitung kerugian moril maupun materiil yang harus ditanggung atas bencana ini.</div>	
</div>
</p>
<p style="margin-left:0px">Tragedi ini telah menimbulkan keprihatinan yang mendalam pada masyarakat Indonesia. Gerakan solidaritas digalang dengan harapan dapat turut meringankan beban para korban bencana. MODENA selaku penyedia peralatan elektronik rumah tangga tidak luput berpartisipasi dengan menghadirkan bantuan serta layanan khusus bagi korban bencana.</p>

<p style="margin-left:0px">Melalui program bertajuk <strong>MODENA Care For Manado</strong> (MODENA Peduli Manado), MODENA aktif membuka pelayanan cuci pakaian gratis bagi masyarakat yang terkena musibah banjir khususnya di kota Manado. Para korban banjir yang masih belum dapat mencuci pakaian dengan layak, dapat membawa pakaian kotornya ke posko MODENA. Hal ini dilatarbelakangi kesadaran akan kebersihan pakaian sangat ditentukan dari proses mencuci yang tidak sekedar hanya membersihkan tapi juga membasmi kuman, juga pada proses pengeringan cucian. Perangkat cuci berupa mesin cuci (<em>washer</em>) dan pengering pakaian (<em>dryer</em>) MODENA menjadi jawaban untuk mendapatkan hasil cucian higienis.</p>

<p style="margin-left:0px">&quot;Tidak hanya kala banjir terjadi, masalah baru kerap kali ditemui pada paska bencana di saat masyarakat kembali ke rumah masing-masing. Sebut saja peralatan elektronik yang tak ayal rusak terkena banjir, atau tumpukan pakaian yang tidak bisa dicuci karena terendam air maupun tidak tersedianya cukup air bersih untuk membersihkannya. Untuk itu kami terpanggil untuk turut berkontribusi pada tahap pemulihan setelah banjir ini&quot;, ujar Desiana Latuconsia, Head of Public Relations, MODENA Indonesia.</p>

<p style="margin-left:0px">Tersedia pula layanan purna jual berupa gratis biaya jasa perbaikan produk MODENA serta potongan harga khusus untuk suku cadang produk yang diperbaiki. Tidak berhenti sampai di situ, terdapat pula program <em>trade-in</em> dengan penawaran spesial untuk menggantikan produk MODENA yang rusak dengan produk sejenisnya.</p>

<p style="margin-left:0px">Kegiatan <strong>MODENA Care for Manado</strong> mengambil lokasi di kantor cabang Jl. Rajawali Kompleks Ruko Terminal Pall Dua Blok C7-C8, Manado, berlangsung mulai tanggal 3 Febuari hingga 8 Febuari 2014. Program yang diberlakukan meliputi servis produk MODENA yang terkena banjir dengan 100% gratis untuk biaya jasa; diskon 35% untuk penggantian suku cadang; serta program <em>trade in</em> dengan potongan harga 30% untuk produk yang sudah tidak dapat diperbaiki lagi. </p>

<p style="margin-left:0px">Selain di Manado, pada Januari lalu juga dibuka <strong>Posko Peduli Banjir MODENA</strong> di Jakarta yang berhasilkan mengumpulkan bantuan yang diperuntukkan bagi para korban banjir.  Ratusan barang-barang kebutuhan pokok seperti popok dalam kemasan, susu kotak UHT, air minum kemasan, roti dan beragam obat-obatan serta bahan pokok lainnya telah berhasil dihimpun dan disalurkan kepada para pengungsi. Pada tanggal 25 Januari 2014, seluruh bantuan tersebut disalurkan kepada para korban banjir di daerah Rawa Jati, kecamatan Pancoran, Jakarta Selatan. Dari enam rukun tetangga yang ada di sana, terdapat sekitar 1100 jiwa pengungsi akibat banjir yang melanda tempat tinggal mereka “Sudah hampir 8 hari saya dan keluarga mengungsi di pinggir jembatan ini. Itu rumah saya yang terlihat hanya atapnya saja.” Ujar pak Ahmad Fatoni selaku Ketua RT 02 kepada perwakilan MODENA.</p>		
		
<p style="margin-left:0px">Air bersih, makanan siap saji seperti roti serta obat demam menjadi hal yang telah dinanti oleh para pengungsi. Ini terlihat dari reaksi para pengungsi yang langsung menggunakan barang-barang bantuan segera setelah diserahkan.
 </p>		
 
 <p style="margin-left:0px">Kegiatan berupa tanggap bencana banjir MODENA diawali pada Januari tahun 2013 lalu saat terjadi bencana banjir di ibukota Jakarta. Di tahun ini MODENA Peduli Manado maupun Posko Peduli Banjir di Jakarta ini merupakan bagian dari aktivitas sosial yang dilakukan di bawah naungan tema <strong>“We Love Society”</strong> yang digadang untuk berbagai kegiatan sosial yang diselenggarakan MODENA sepanjang tahun 2014.</p>
 
 <p style="margin-left:0px">“Kegiatan ini diharapkan dapat meringankan beban para korban banjir, serta menghadirkan solusi praktis dalam masa pemulihan bagi mereka agar dapat mempersiapkan diri untuk kembali pada kehidupan dan rutinitas seperti sediakala”, tuntas Desiana.</p>
